package org.arios.plugin;

/**
 * Represents the plugin types.
 * @author Emperor
 */
public enum PluginType {

	/**
	 * Action plugin type.
	 */
	ACTION,

	/**
	 * Dialogue plugin type.
	 */
	DIALOGUE,

	/**
	 * Command plugin type.
	 */
	COMMAND,

	/**
	 * Activity plugin type.
	 */
	ACTIVITY,

	/**
	 * Random event plugin type.
	 */
	RANDOM_EVENT,

	/**
	 * Abstract NPC plugin type.
	 */
	ABSTRACT_NPC,

	/**
	 * Player login plugin type.
	 */
	LOGIN,

	/**
	 * Player logout plugin type.
	 */
	LOGOUT;

}